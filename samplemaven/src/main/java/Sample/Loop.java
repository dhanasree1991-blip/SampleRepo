public class Loop {

	
	

    public static void main(String[] args) {

        for (int i = 1; i <= 6; i++) {
            System.out.println(i + "");
        }
        int j=1;
        do {
        	System.out.println(j);
        	j++;
        	}while (j<=5);
        int [] numbers = {10,20,30};
        for (int n :numbers) {
        	System.out.println(n);
        }
        }
        
    }

